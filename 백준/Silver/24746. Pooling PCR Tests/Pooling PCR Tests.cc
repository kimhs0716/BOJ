constexpr ld eps = 1e-8;
void solve(ll testcase) {
    ll i, j, k;
    ld p; cin>>p;
    ld min_val = 1;
    ll ans=1;
    for (ll N=2;N<=16;N++) {
        ld P = pow(1-p, N);
        ld ET = P + (1-P)*N;
        // cout<<ET/N<<endl;
        if (ET/N+eps < min_val) {
            min_val = ET/N;
            ans = N;
        }
    }
    cout<<ans<<endl;
}

