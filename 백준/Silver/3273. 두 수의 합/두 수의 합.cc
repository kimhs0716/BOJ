constexpr ll MAX = 1'000'000;
ll cnt[MAX+1] = {0, };
void solve(ll testcase) {
    ll i, j, k;
    ll n; cin>>n;
    vl arr(n); cin>>arr;
    for (i=0;i<n;i++) cnt[arr[i]]++;
    ll x; cin>>x;
    ll ans=0;
    for (i=0;i<=x/2;i++) {
        if (2*i==x) ans += cnt[i]*(cnt[i]-1)/2;
        else if (x-i<=MAX) ans += cnt[i] * cnt[x-i];
    }
    cout<<ans<<endl;
}

