#include <stdio.h>

long long int arr[2][2]={};
long long int map[2][2]={};

long long int f()
{
    long long int x,y,z,w;
    x=(arr[0][0]*map[0][0])%1000000000+(arr[0][1]*map[1][0])%1000000000;
    y=(arr[0][0]*map[0][1])%1000000000+(arr[0][1]*map[1][1])%1000000000;
    z=(arr[1][0]*map[0][0])%1000000000+(arr[1][1]*map[1][0])%1000000000;
    w=(arr[1][0]*map[0][1])%1000000000+(arr[1][1]*map[1][1])%1000000000;
    map[0][0]=x%1000000000;
    map[0][1]=y%1000000000;
    map[1][0]=z%1000000000;
    map[1][1]=w%1000000000;
}


long long int g()
{
    long long int x,y,z,w;
    x=(arr[0][0]*arr[0][0])%1000000000+(arr[0][1]*arr[1][0])%1000000000;
    y=(arr[0][0]*arr[0][1])%1000000000+(arr[0][1]*arr[1][1])%1000000000;
    z=(arr[1][0]*arr[0][0])%1000000000+(arr[1][1]*arr[1][0])%1000000000;
    w=(arr[1][0]*arr[0][1])%1000000000+(arr[1][1]*arr[1][1])%1000000000;
    arr[0][0]=x%1000000000;
    arr[0][1]=y%1000000000;
    arr[1][0]=z%1000000000;
    arr[1][1]=w%1000000000;
}

int main()
{
    long long int a, b, x, y, i;
    scanf("%lld %lld", &a, &b);
    b+=1;
    arr[0][0]=1;
    arr[1][0]=1;
    arr[0][1]=1;
    map[0][0]=1;
    map[1][1]=1;
    i=0;
    while(i<70)
    {
        if(a%2==1) f();
        g();
        a/=2;
        i++;
    }
    x=map[0][0]-1;
    arr[0][0]=1;
    arr[1][0]=1;
    arr[0][1]=1;
    arr[1][1]=0;
    map[0][0]=1;
    map[0][1]=0;
    map[1][1]=1;
    map[1][0]=0;
    i=0;
    while(i<70)
    {
        if(b%2==1) f();
        g();
        b/=2;
        i++;
    }
    y=map[0][0]-1;
    if(y-x<0) y+=1000000000;
    printf("%lld", y-x);
    return 0;
}
