void solve(ll tc) {
    ll i, j;
    ll r, c, a, b; cin>>r>>c>>a>>b;
    a %= 10;
    b %= 10;
    ll ans = 0;
    for (ll w=0;w<10;w++) {
        if (w+1>c) break;
        ll h = ok[a][b][w];
        if (h==9) h=10;
        if (h>r) h=r;
        for (ll hh=1;hh<=h;hh++) {
            // cout<<w+1<<' '<<hh<<endl;
            ans += (c-w)*(r-hh+1);
        }
    }
    cout<<ans<<' ';
}

